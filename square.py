def area(a):
    """
    Returns area of square
    :param a: size of square's side
    :type a: float:

    :rtype: float
    :return: area of square
    """
    return a * a


def perimeter(a):
    """
    Returns perimeter of square
    :param a: size of square's side
    :type a: float

    :rtype: float
    :return: perimeter of square
    """
    return a * 4
