def area(a, b): 
    """
    Returns area of rectangle
    :param a: size of first side
    :type a: float
    :param b: size of second side
    :type b: float

    :rtype: float
    :return: area of rectangle
    """
    return a * b 


def perimeter(a, b):
    """
    Returns perimeter of rectangle
    :param a: size of first side
    :type a: float
    :param b: size of second side
    :type b: float

    :rtype: float
    :return: perimeter of rectangle
    """
    return (a + b) * 2
